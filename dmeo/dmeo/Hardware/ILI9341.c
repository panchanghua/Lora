/****************************************************************************
* 名    称：void ili9341_DrawPicture(u16 StartX,u16 StartY,u16 EndX,u16 EndY,const unsigned char *pic)
* 功    能：在指定座标范围显示一副图片
* 入口参数：StartX     行起始座标
*           StartY     列起始座标
*           EndX      行结束座标
*           EndY       列结束座标
            pic             图片头指针
* 出口参数：无
* 说    明：图片取模格式为水平扫描，16位颜色模式
* 调用方法：ili9320_DrawPicture(0,0,100,100,(u16*)demo);
****************************************************************************/
void LCD_ili9341_DrawPicture(uint16_t StartX,uint16_t StartY,uint16_t Xend,uint16_t Yend,const unsigned char *pic,uint8_t mode)
{
    uint32_t i=0,j=0;
	uint8_t temp = 0;
	uint32_t piont = 0,zheng = 0,yu= 0;


	uint16_t *bitmap = (uint16_t *)pic;
	uint8_t *bitmap1 = (uint8_t *)pic;

	if(mode == 1)
	{
    
	    LCDOpenWindows(StartX,StartY,Xend-StartX,Yend-StartY);
	    
	    for(i=StartY;i<Yend;i++)
	    {
	        for(j=StartX;j<Xend;j++)
			{
				LCD_Write_Data(*bitmap++); 
			}        
	    }
	}else if(mode ==4)
		{
			LCDOpenWindows(StartX,StartY,Xend-StartX,Yend-StartY);
			piont = (Xend-StartX) * (Yend-StartY);
			zheng = piont/8;
			yu = piont%8;

			for(i= 0 ; i<zheng; i++)
			{
				temp = *bitmap1;
				for(uint8_t u=0;u<8;u++)
				{
					if((temp & 0x80) == 0x00)
					{
						LCD_Write_Data(0xd6db);
					}else
						{
							LCD_Write_Data(0x0a93);
						}
					temp = temp<<1;
				}
				bitmap1++;   
			}

			for(j=0;j<yu;j++)
			{
				temp = *bitmap1;
				for(uint8_t u=0;u<8;u++)
				{
					if((temp & 0x80) == 0x00)
					{
						LCD_Write_Data(0xd6db);
					}else
						{
							LCD_Write_Data(0x0a93);
						}
					temp = temp<<1;
				} 
			}
		  
		}
    
}

