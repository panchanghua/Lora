void  LCD_WriteCommand(uint8_t c)
{
	uint8_t data[1] ;
	SPI_HandleTypeDef *spi1 = &hspi1;

	data[0] = c;
	
     DC_LOW();
     CS_LOW();
	 
     HAL_SPI_Transmit(spi1,data,1,2000);
	 
     CS_HIGH();	
}

//========================================================

void LCD_WriteData(uint8_t dat)
{
	uint8_t data[1] ;
	SPI_HandleTypeDef *spi1 = &hspi1;

	data[0] = dat;
	
      DC_HIGH();
      CS_LOW();
	  
      HAL_SPI_Transmit(spi1,data,1,2000);
	 
      CS_HIGH();
}

